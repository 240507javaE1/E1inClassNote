class Ex4
{
	public static void main(String[] args)
	{
		System.out.println(10+3);
		System.out.println(10/3);

		int x=10;
		int y=3;

		System.out.println(x+"+"+y+"="+(x+y));
		System.out.println(x+"-"+y+"="+(x-y));
		System.out.println(x+"*"+y+"="+(x*y));
		System.out.println(x+"/"+y+"="+(x/y));
		System.out.println(x+"%"+y+"="+(x%y));
	}
}